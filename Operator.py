##Logical Operator 

#AND- any one is false then gives false, otherwise true
x=True
y=True
print("AND :",x and y) #True 

#OR- any one is True then gives True , Otherwise False
P=False
Q=True
print("OR :",P or Q)#True

#NOT- only reverse
s=True
print("NOT :",not s)#False